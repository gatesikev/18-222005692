//Java Program to demonstrate the use of break statement    
     public class breakstatement{ 

    public static void main(String[] args) {  
        //using for loop  
        for(int i=1;i<=15;i++){  
            if(i==8){  
                //breaking the loop  
                break;  
            }  
            System.out.println(i);  
        }  
    }  
    }  

